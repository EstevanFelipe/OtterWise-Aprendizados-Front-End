// String
console.log('String'); // String = impresso no console.
// (String), variável que armazena textos e aFirmações.

// Number
console.log(12345); // Number = impresso no console.
// (Number), variável que armazena números.

// Boolean
console.log(true); // Boolean = impresso no console.
console.log(false); // Boolean = impresso no console.
// (Boolean), variável que armazena valores lógicos, true ou false.

// Null
console.log(null); // Null = impresso no console.
// (Null), variável que armazena valores nulos.

// Undefined
console.log(undefined); // Undefined = impresso no console.
// (Undefined), variável que armazena valores indefinidos.