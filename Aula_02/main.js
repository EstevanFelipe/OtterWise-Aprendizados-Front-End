// Exemplos de Expressões Aritméticas:
const soma = 1 + 10;
console.log(soma); // 2 = impresso no console.

const subtracao = 10 - 1;
console.log(subtracao); // 9 = impresso no console.

const multiplicacao = 10 * 10;
console.log(multiplicacao); // 100 = impresso no console.

const divisao = 10 / 2;
console.log(divisao); // 5 = impresso no console.

const resto = 10 % 2;
console.log(resto); // 0 = impresso no console.

const potencia = 10 ** 2;
console.log(potencia); // 100 = impresso no console.
